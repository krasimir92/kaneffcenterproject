		<p>Почетният консул на Република България в Мисисага Игнат Канев бе лично удостоен от генерал-губернатора на Канада Дейвид Джонстън със званието
		„Член на ордена на Канада". Награждаването се случи на 12 май в официалната резиденция на генерал-губернатора в Отава. </p>
		
		<p>По време на церемонията генерал-губернаторът заяви, че с удоволствие връчва „Ордена на Канада" на неговите нови членове - в годината, 
		в която се отбелязват не само 150 години от създаването на Канадската конфедерация, но също така се празнува и 50-годишнината от учредяването на ордена.
		„Нека бъдем вдъхновени от примера на тези забележителни канадци и да използваме случая, за да градим по-интелигентна и по-грижовна страна, 
		в която всеки отделен гражданин да постигне възможно най-големите успехи".</p>
		<p>В лично обръщение към Игнат Канев генерал-губ 	рнаторът отчете заслугите му 
		в областта на строителството на жилища и предприемачеството в продължение на 50 години и го определи като „двигател на икономическия растеж". 
		Бе откроена неговата роля на катализатор на инициативи в секторите образование, здравеопазване, социални услуги и изкуство.</p> 
		<p>Изразено бе признание за неговата щедрост и филантропска дейност, спомогнали за обогатяването на живота на жители на неговия град и на граждани на България, 
		неговата родна страна.</p>
		<p>Във връзка с награждаването ден преди това, Канев беше поканен в Сената на Канада. Сенаторът от провинция Онтарио Виктор Оу, 
		зам.-председател на Групата за приятелство с България в парламента, прочете пред пленарен състав на Сената поздравителен адрес, 
		изтъквайки заслугите на Канев и приноса, заради който той е награден с най-високото канадско отличие.</p>
		<p>Сенатът изрази почит към Игнат Канев със ставане на крака и ръкопляскания. Преди церемонията в Сената Игнат Канев беше лично поздравен от председателите на двете камари на парламента, 
		от съпредседателя на групата за приятелство сенатор Йона Мартин и от много депутати и сенатори.</p>
		<p><i>Създаден през 1967 г. 
		от Нейно Величество кралица Елизабет II „Орденът на Канада" е крайъгълен камък в канадската наградна система и се присъжда за изключителни постижения, 
		отдаденост към обществото и служба на нацията. От учредяването му до днес повече от шест хиляди души от всички сфери на канадското общество са 
		били удостоени с отличието. Мотото на ордена е DESIDERANTES MELIOREM PATRIAM (Те желаят една по-добра страна). Нейно Величество е суверен на ордена, 
		а генерал-губернаторът е негов канцлер и главен кавалер. Званието "Член на ордена на Канада", една от трите степени на отличието, 
		признава изключителния принос на местно и регионално ниво или специална сфера на дейност. Символът на ордена е изработен от фино сребро и 
		има три индивидуални  фрагмента: снежинка, пръстеновиден елемент и кленово листо. Цветовете се добавят допълнително на ръка 
		чрез уникално приложение на матов и прозрачен емайл. Дизайнът на отличителните знаци на „Ордена на Канада" датира от 1967 г. 
		Техническите чертежи, използвани от Кралския канадски монетен двор в Отава за това ново поколение отличителни знаци, 
		са били изработени от канадските хералдически власти към секретариата на генерал-губернатора.</i></p>

		<p align="right"><b>Текст и снимка: посолство на Република България в Отава, Канада</b></p>
		
