		<p>Игнат Христов Кънев е роден на 6 октомври 1926 г. в с. Горно Абланово, Русенска област. Той е четвъртото от 7-те деца 
		(Къньо, Ламби, Куна, Игнат, Мария, Симеон и Петър) на Христо и Мита Къневи.</p>
        <p>Няма финансовата възможност да учи, а е принуден още на 14-годишна възраст да напусне България и да се установи в Австрия, за да търси препитание. 
		През март 1941 г. той с още няколко момчета отиват при Никола Парушчолаков, градинар от Горна Оряховица, 
		който има зеленчукови градини в Австрия и търгува със земеделски стоки. След 1945 г. го разубеждават да се върне в България. 
		След 5 години чиракуване вече има собствена търговия и малко производство на плодове и зеленчуци.</p>
		<p>През 1951 г. зарязва бизнеса в Австрия и почти без средства, без да знае английски и без да има някакви връзки и приятели, заминава за Канада. 
		Установява се в Торонто, където навлиза в строителния бизнес. Първоначално строи малки къщи за новопристигащите емигранти. 
		Пет години по-късно, през 1956 г., вече основава там първата си строителна фирма. Следващата година строи първата си по-висока страда – 
		на 3 етажа с 9 апартамента, а 10 години по-късно вече вдига сграда с 262 жилища. Впоследствие годишният оборот на компанията му достига 
		над 250 млн. долара. По време на своята активна дейност като строителен предприемач, той издига хиляди къщи, 
		много обществени сгради и голф-съоръжения от световна класа.</p>
		<p>Занимава се и с търговия на автомобили. Става представител на "Дженерал мотърс" и продава хиляди коли и камиони.</p>
		<p>Собственик е на 6 голф игрища и голяма строителна фирма в Канада. Управител е на благотворителна фондация, носеща неговото име.</p>
